$("#btn-verContrasenia").click(function(){
  if("password" == $('#inputPassword').attr("type")){
    $('#inputPassword').removeAttr('type');
    $('#inputPassword').attr({type:"text"});
  }else{
    $('#inputPassword').removeAttr('type');
    $('#inputPassword').attr({type:"password"});
  }
});